package br.com.controlefrota.model;

public class RelatorioConsumosModel {

	private Float totalConsumosEmReais;
	private Float totalLitrosAbastecidos;
	private long totalDeNotasFiscais;
	
	
	
	
	public Float getTotalConsumosEmReais() {
		return totalConsumosEmReais;
	}
	public void setTotalConsumosEmReais(Float totalConsumosEmReais) {
		this.totalConsumosEmReais = totalConsumosEmReais;
	}
	public Float getTotalLitrosAbastecidos() {
		return totalLitrosAbastecidos;
	}
	public void setTotalLitrosAbastecidos(Float totalLitrosAbastecidos) {
		this.totalLitrosAbastecidos = totalLitrosAbastecidos;
	}
	public long getTotalDeNotasFiscais() {
		return totalDeNotasFiscais;
	}
	public void setTotalDeNotasFiscais(long totalDeNotasFiscais) {
		this.totalDeNotasFiscais = totalDeNotasFiscais;
	}
	
	
}
